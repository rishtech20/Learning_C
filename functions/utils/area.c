/*The following function takes radius of the circles and returns its area*/

double AreaCircle(double r){
    double area;
    area = 3.14 * r * r;
    return area;
}

/*The following function takes in length of the base and the height of a triangle and
returns its area*/

double AreaTriangle(double base, double height){
    double area;
    area = 0.5 * base * height;
    return area;
    return area;
}
